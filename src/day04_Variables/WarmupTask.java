package day04_Variables;

public class WarmupTask {
	
	/*
	 1. Cucumber $ 2.5
	 2. Tomatoes $ 3
	 3. Onion    $ 1.5
	 4. Pepper   $ 2
	 5. Carrot   $ 4
	 */
	
	public static void main(String[] args) {
		
		//Cucumber 2.2
		float Cucumber = 2.5f;
		double Cucumber2 = 2.5f; // double can contain float data
		System.out.println(Cucumber);
		System.out.println(Cucumber2);
		
		// Onion
		float Onion = 1.5f;
		double Onion2 = 1.5; 
		System.out.println(Onion2);
		System.out.println(Onion);
		
		//Pepper
		byte Pepper = 2;
		short Pepper2 = 2;
		int Pepper3 = 2;
		long Pepper4 = 2;
		float Pepper5 = 2;
		double Pepper6 = 2;
		
		System.out.println(Pepper);
		System.out.println(Pepper2);
		System.out.println(Pepper3);
		System.out.println(Pepper4);
		System.out.println(Pepper5);
		System.out.println(Pepper6);
		
	}

}
